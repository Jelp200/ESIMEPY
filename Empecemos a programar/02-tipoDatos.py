""" DATOS EN PY
Tenemos diferentes tipos de datos en python
    - Numericos: int, float, complejos
    - Diccionario
    - Set
    - De tipo secuencia: string, listas, tuplas

    Pero nosotros veremos por el momento: int, float, string, booleanos y null
"""

# Entero
x = 13; print(x, end=' es de tipo '); print(type(x))

# Flotante
y = 5.1613; print(y, end=' es de tipo '); print(type(y))

# String
z = 'K'; print(z, end=' es de tipo '); print(type(z))                                               # En otros lenguajes de programación esto es un caracter
a = "ESIME Z | IPN"; print(a, end=' es de tipo '); print(type(a))

# Booleano
b = True; print(b, end=' es de tipo '); print(type(b))
c = False; print(c, end=' es de tipo '); print(type(c))

# Vacio
d = None; print(d, end=' es de tipo '); print(type(d))

